// Data Types

// Number : FLoating point numbers Used for decimal and integers
let age = 23;
console.log("let age = 23 ", typeof age);

// String : Sequence of characters Used for text
let firstName = "Koushik";
console.log("let firstName = Koushik ", typeof firstName);

// Boolean : Logical type that can only be true or false Used for taking decisions
let fullAge = true;
console.log("let fullAge = true ", typeof fullAge);

// Undefined : Value taked by a variable that is not yet defined("empty value")
let childed;
console.log("let childed ", typeof childed);

// Null : Also means "empty value"
console.log("null ", typeof null);

